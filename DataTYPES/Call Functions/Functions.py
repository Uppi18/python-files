
# Function definition
def greet():
    print("Hello!")

# Function call
greet()   # Output: Hello!

def greet(name):
    print(f"Hello, {name}!")

greet("Alice")      # Output: Hello, Alice!
greet("Upendra")    # output; Hello, upendra

